package com.conditional.statements;
/*While Loop - First Check Condition & Then Execute Statement
 * Do While - Execute Statement at least once & Then Move to condition
 * do..while
	------------
	do
	{
	statements;
	}while(condition);*/
//First it checks condition and then enters into the block

//print 1 to 10 numbers
public class Do_While_Loop {
	public static void main(String[] args) {
		
		
		//int i =1;
		int i =100;
		do
		{
			System.out.println(i);//it will execute statement before checking condtion
			i++;//incrment or else it will enter into loop
		}while (i<=10);
		
	}
	 
	
		
	}
	


