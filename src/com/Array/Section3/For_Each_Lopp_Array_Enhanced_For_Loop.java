package com.Array.Section3;
//For Each Loop is also known as Enhanced For Loop
//No Incrementation required
public class For_Each_Lopp_Array_Enhanced_For_Loop {
	public static void main(String[] args) {
		
		int e [] = {100, 200, 300, 400, 500, 600};
		//Enhanced For Loop
		for (int i:e)
		{
			System.out.println(i);
		}
		
		
	}
}
